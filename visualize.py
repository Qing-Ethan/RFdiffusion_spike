def main():
    # 坐标点列表


    # 绘制旋转向量图
    def plot_rotation_vectors(coordinate):
        fig = go.Figure()

        # 添加原点
        fig.add_trace(go.Scatter3d(x=[0], y=[0], z=[0], mode='markers', marker=dict(color='red'), name='Origin'))

        # 添加坐标点
        for i, coord in enumerate(coordinate):
            fig.add_trace(go.Scatter3d(x=[coord[0]], y=[coord[1]], z=[coord[2]], mode='markers', marker=dict(color='blue'), name=f'Coordinate {i}'))

            # 添加旋转向量
            fig.add_trace(go.Cone(x=[0], y=[0], z=[0], u=[coord[0]], v=[coord[1]], w=[coord[2]], colorscale='Greens', sizemode='absolute', sizeref=1, name=f'Rotation Vector {i}'))

        # 设置图形布局
        fig.update_layout(scene=dict(xaxis_title='X', yaxis_title='Y', zaxis_title='Z'))

        # 显示图形
        fig.show()

    # 绘制旋转向量图
    plot_rotation_vectors(coordinates)

